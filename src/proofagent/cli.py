def skill_run(model, skill_name, pack_path, judge, json_out, provider_name, endpoint_url, headers_str, endpoint_format):
    """Run skill proof evaluation against a model.

    Tests competence in coding, reasoning, math, writing, and summarization.
    Each challenge is scored 0-10 by an LLM judge against a rubric.

    Use --pack to load a custom skill pack from a YAML file. When both
    --skill and --pack are provided, both built-in and custom packs are run.

    \b
    Examples:
        proofagent skill run claude-sonnet-4-6 --skill coding
        proofagent skill run gpt-4o --skill all
        proofagent skill run --endpoint https://my-agent.com/api/chat --skill coding
        proofagent skill run claude-sonnet-4-6 --pack ./my_skills.yaml
    """
    import json as json_mod

    from proofagent import display
    from proofagent.providers import get_provider
    from proofagent.skills import (
        SKILL_PACKS,
        load_custom_pack,
        run_skill_report,
        save_skill_report,
    )

    display_model = model if model != "endpoint" else (endpoint_url or "endpoint")

    click.echo()
    click.echo(f"  {display.BOLD}proofagent skill run{display.RST} — Skill Proof")
    click.echo(f"  Model: {display_model}")
    click.echo(f"  Judge: {judge}")
    click.echo()

    # Load custom pack if provided
    custom_pack_key = None
    if pack_path is not None:
        try:
            custom_data = load_custom_pack(pack_path)
        except (FileNotFoundError, ValueError) as e:
            click.echo(f"  {display.fail_text('Error:')} {e}")
            sys.exit(1)
        # Register under a sanitized key so the runner can find it
        import re as _re
        custom_pack_key = "custom_" + _re.sub(r'[^\w]', '_', custom_data["name"].lower())
        SKILL_PACKS[custom_pack_key] = custom_data
        click.echo(f"  Custom pack: {custom_data['name']} ({len(custom_data['challenges'])} challenges)")

    # Determine which built-in skills to run
    skills: list[str] = []
    if pack_path is not None and skill_name == "all":
        # --pack without explicit --skill: run only the custom pack
        skills = []
    elif skill_name == "all":
        skills = [k for k in SKILL_PACKS.keys() if not k.startswith("custom_")]
        click.echo(f"  Running {len(skills)} skill packs: {', '.join(skills)}")
    else:
        if skill_name not in SKILL_PACKS:
            available = ", ".join(k for k in SKILL_PACKS.keys() if not k.startswith("custom_"))
            click.echo(f"  {display.fail_text('Error:')} Unknown skill '{skill_name}'. Available: {available}")
            sys.exit(1)
        skills = [skill_name]
        click.echo(f"  Skill: {SKILL_PACKS[skill_name]['name']}")

    # Append custom pack
    if custom_pack_key is not None:
        skills.append(custom_pack_key)

    if not skills:
        click.echo(f"  {display.fail_text('Error:')} No skills to run.")
        sys.exit(1)

    click.echo()

    try:
        if endpoint_url:
            headers = _parse_headers(headers_str)
            endpoint_provider = get_provider("endpoint", url=endpoint_url, headers=headers, format=endpoint_format)
            report = run_skill_report(model, skills=skills, judge_model=judge, provider=endpoint_provider)
        else:
            if model == "endpoint":
                click.echo(f"  {display.fail_text('Error:')} Provide a model name or use --endpoint <url>")
                sys.exit(1)
            report = run_skill_report(model, skills=skills, judge_model=judge, provider=provider_name)
    except Exception as e:
        click.echo(f"  {display.fail_text('Error:')} {e}")
        sys.exit(1)

    if json_out:
        click.echo(json_mod.dumps(report.to_dict(), indent=2))
        return

    # Terminal display
    click.echo(f"  {'Skill':<18} {'Score':>7} {'Grade':>7}")
    click.echo(f"  {'-' * 18} {'-' * 7} {'-' * 7}")

    for proof in report.proofs:
        score_str = f"{proof.avg_score:.1f}/10"
        if proof.passed:
            score_str = display.pass_text(score_str)
            grade_str = display.pass_text(proof.grade)
        else:
            score_str = display.fail_text(score_str)
            grade_str = display.fail_text(proof.grade)

        click.echo(f"  {proof.skill_name:<18} {score_str:>19} {grade_str:>19}")

        # Show individual challenges
        for r in proof.results:
            indicator = display.pass_text("✓") if r.score >= 6 else display.fail_text("✗")
            click.echo(
                f"    {indicator} {r.challenge.prompt[:50]:<50} "
                f"{display.DIM}{r.score}/10 — {r.reason[:60]}{display.RST}"
            )

    click.echo()

    # Overall
    overall = report.overall_score
    grade = report.overall_grade
    if overall >= 6.0:
        click.echo(f"  Overall: {display.pass_text(f'{overall:.1f}/10')} — Grade: {display.pass_text(grade)}")
    else:
        click.echo(f"  Overall: {display.fail_text(f'{overall:.1f}/10')} — Grade: {display.fail_text(grade)}")

    total_cost = sum(p.total_cost for p in report.proofs)
    click.echo(f"  Cost: {display.cost_text(total_cost)}")

    # Save
    output_file = save_skill_report(report)
    click.echo(f"  Saved: {output_file}")
    click.echo(f"  Fingerprint: {report.fingerprint}")
    click.echo()

    if not all(p.passed for p in report.proofs):
        sys.exit(1)


@skill.command(name="badge")
@click.argument("proof_path")
@click.option(
    "--output", "-o", "output_path", default="proofagent-badge.svg",
    help="Output SVG file path (default: proofagent-badge.svg)",
)
def skill_badge(proof_path, output_path):
    """Generate an SVG badge from a skill report JSON file.

    PROOF_PATH is the path to a skill report JSON (e.g.
    .proofagent/skills/skill_claude-sonnet-4-6_*.json).

    Examples:
        proofagent skill badge .proofagent/skills/skill_claude-sonnet-4-6_20260318_120000.json
        proofagent skill badge .proofagent/skills/skill_claude-sonnet-4-6_20260318_120000.json -o badge.svg
    """
    import json as json_mod
    from pathlib import Path

    from proofagent import display
    from proofagent.badge import generate_badge_html, generate_badge_markdown, generate_badge_svg

    path = Path(proof_path)
    if not path.exists():
        click.echo(f"  {display.fail_text('Error:')} File not found: {proof_path}")
        sys.exit(1)

    try:
        with open(path) as f:
            data = json_mod.load(f)
    except json_mod.JSONDecodeError as e:
        click.echo(f"  {display.fail_text('Error:')} Invalid JSON: {e}")
        sys.exit(1)

    grade = data.get("overall_grade", "F")
    score = data.get("overall_score", 0.0)
    model = data.get("model", "unknown")

    click.echo()
    click.echo(f"  {display.BOLD}proofagent skill badge{display.RST}")
    click.echo()
    click.echo(f"  Model: {model}")
    click.echo(f"  Grade: {grade} ({score:.1f}/10)")
    click.echo()

    svg = generate_badge_svg(label="proofagent", grade=grade, score=score)

    out = Path(output_path)
    out.write_text(svg, encoding="utf-8")
    click.echo(f"  Saved: {out}")
    click.echo()

    # Print embed codes
    safe_model = model.replace("/", "-")
    md = generate_badge_markdown(safe_model, grade)
    html = generate_badge_html(safe_model, grade)

    click.echo(f"  {display.BOLD}Markdown:{display.RST}")
    click.echo(f"  {md}")
    click.echo()
    click.echo(f"  {display.BOLD}HTML:{display.RST}")
    click.echo(f"  {html}")
    click.echo()


@skill.command(name="submit")
@click.argument("proof_path")
def skill_submit(proof_path):
    """Submit a skill proof to the leaderboard.

    PROOF_PATH can be a local file path or a URL to a proof JSON.
    If the model already exists on the leaderboard, it will be updated.

    Examples:
        proofagent skill submit .proofagent/skills/skill_claude-sonnet-4-6_20260318_120000.json
        proofagent skill submit https://example.com/my-proof.json
    """
    import json as json_mod
    from pathlib import Path

    from proofagent import display

    click.echo()
    click.echo(f"  {display.BOLD}proofagent skill submit{display.RST}")
    click.echo()

    # Load proof from file or URL
    proof_data = None

    if proof_path.startswith("https://"):
        click.echo(f"  Fetching: {proof_path}")
        try:
            import urllib.request
            with urllib.request.urlopen(proof_path) as resp:
                proof_data = json_mod.loads(resp.read().decode())
        except Exception as e:
            click.echo(f"  {display.fail_text('Error:')} Failed to fetch URL: {e}")
            sys.exit(1)
    elif proof_path.startswith("http://"):
        click.echo(f"  \033[33mWarning:\033[0m URL uses http:// (not https). Proceeding anyway.")
        click.echo(f"  Fetching: {proof_path}")
        try:
            import urllib.request
            with urllib.request.urlopen(proof_path) as resp:
                proof_data = json_mod.loads(resp.read().decode())
        except Exception as e:
            click.echo(f"  {display.fail_text('Error:')} Failed to fetch URL: {e}")
            sys.exit(1)
    elif "://" in proof_path:
        click.echo(f"  {display.fail_text('Error:')} Unsupported URL scheme. Only http:// and https:// are allowed.")
        sys.exit(1)
    else:
        path = Path(proof_path)
        if not path.exists():
            click.echo(f"  {display.fail_text('Error:')} File not found: {proof_path}")
            sys.exit(1)
        try:
            with open(path) as f:
                proof_data = json_mod.load(f)
        except json_mod.JSONDecodeError as e:
            click.echo(f"  {display.fail_text('Error:')} Invalid JSON: {e}")
            sys.exit(1)

    # Validate proof structure
    if not proof_data.get("model"):
        click.echo(f"  {display.fail_text('Error:')} Proof missing 'model' field.")
        sys.exit(1)

    skills = proof_data.get("skills", [])
    if not skills:
        click.echo(f"  {display.fail_text('Error:')} Proof missing 'skills' array.")
        sys.exit(1)

    required = {"coding", "reasoning", "math", "writing", "summarization"}
    found = {s.get("skill") for s in skills}
    missing = required - found

    if missing:
        click.echo(f"  {display.fail_text('Error:')} Missing skill proofs: {', '.join(missing)}")
        click.echo(f"  Run: proofagent skill {proof_data['model']} --skill all")
        sys.exit(1)

    # Validate scores
    for s in skills:
        avg = s.get("avg_score", 0)
        if not (0 <= avg <= 10):
            click.echo(f"  {display.fail_text('Error:')} Invalid score {avg} for {s.get('skill')}")
            sys.exit(1)

    # Display what we're submitting
    click.echo(f"  Model: {display.BOLD}{proof_data['model']}{display.RST}")
    click.echo(f"  Fingerprint: {proof_data.get('fingerprint', 'none')}")
    click.echo()

    for s in skills:
        score = s["avg_score"]
        grade = s["grade"]
        if score >= 8:
            score_str = display.pass_text(f"{score:.1f}/10")
            grade_str = display.pass_text(grade)
        elif score >= 6:
            score_str = f"{display.YELLOW}{score:.1f}/10{display.RST}"
            grade_str = f"{display.YELLOW}{grade}{display.RST}"
        else:
            score_str = display.fail_text(f"{score:.1f}/10")
            grade_str = display.fail_text(grade)
        click.echo(f"  {s['skill_name']:<18} {score_str:>19}  {grade_str:>19}")

    click.echo()

    overall = proof_data.get("overall_score", 0)
    overall_grade = proof_data.get("overall_grade", "?")
    click.echo(f"  Overall: {overall:.1f}/10 — Grade: {overall_grade}")
    click.echo()

    # Save to leaderboard data file
    lb_file = Path(".proofagent/leaderboard.json")
    lb_file.parent.mkdir(parents=True, exist_ok=True)

    leaderboard = []
    if lb_file.exists():
        try:
            with open(lb_file) as f:
                leaderboard = json_mod.load(f)
        except (json_mod.JSONDecodeError, ValueError):
            leaderboard = []

    # Check for existing entry
    entry = {
        "model": proof_data["model"],
        "overall_score": overall,
        "overall_grade": overall_grade,
        "fingerprint": proof_data.get("fingerprint", ""),
        "timestamp": proof_data.get("timestamp", ""),
        "skills": {s["skill"]: s["avg_score"] for s in skills},
    }

    existing_idx = None
    for i, e in enumerate(leaderboard):
        if e.get("model", "").lower() == entry["model"].lower():
            existing_idx = i
            break

    if existing_idx is not None:
        old_score = leaderboard[existing_idx].get("overall_score", 0)
        leaderboard[existing_idx] = entry
        click.echo(f"  {display.pass_text('Updated')} {entry['model']} on leaderboard ({old_score:.1f} -> {overall:.1f})")
    else:
        leaderboard.append(entry)
        click.echo(f"  {display.pass_text('Added')} {entry['model']} to leaderboard")

    # Sort by overall score descending
    leaderboard.sort(key=lambda e: e.get("overall_score", 0), reverse=True)

    with open(lb_file, "w") as f:
        json_mod.dump(leaderboard, f, indent=2)

    click.echo(f"  Saved: {lb_file}")
    click.echo()

    # Show the ranking
    click.echo(f"  {'#':<4} {'Model':<25} {'Score':>8} {'Grade':>7}")
    click.echo(f"  {'-' * 4} {'-' * 25} {'-' * 8} {'-' * 7}")

    for i, e in enumerate(leaderboard, 1):
        s = e.get("overall_score", 0)
        g = e.get("overall_grade", "?")
        marker = " <-" if e["model"].lower() == entry["model"].lower() else ""
        click.echo(f"  {i:<4} {e['model']:<25} {s:>7.1f} {g:>7}{marker}")

    click.echo()
    click.echo(f"  Submit to proofagent.dev: {display.DIM}coming soon — paste proof URL on /skills{display.RST}")
    click.echo()


